    private void filltable() {
        tab.setRowCount(0);
        try {
            PreparedStatement stmt;
            stmt = con.prepareStatement("select *from attendance");
            
            ResultSet rs;
             
            rs = stmt.executeQuery();
            while (rs.next()) {
                tab.addRow(new Object[]{rs.getInt(1), rs.getString(2), rs.getString(3), rs.getString(4)});

            }
            table.setModel(tab);
        } catch (SQLException ex) {
            Logger.getLogger(ADDGU.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

