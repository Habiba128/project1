    private void submitbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitbuttonActionPerformed
        if (!edt_name.getText().isEmpty() && !edt_id.getText().isEmpty() && !edt_date.getText().isEmpty()) {
            String name = edt_name.getText();
            String date = edt_date.getText();
            int id = Integer.parseInt(edt_id.getText());
            String statue;
            if (r_apsent.isSelected()) {
                statue = "apsent";
            } else {
                statue = "present";
            }

            try {
                PreparedStatement stmt;
                stmt = con.prepareStatement("insert into attendance (std_id,dt,statue,username) values(?,?,?,?)");
                
                  
                stmt.setInt(1, id);
                stmt.setString(2, date);
                stmt.setString(3,statue);
                stmt.setString(4, name);
                stmt.executeUpdate();
                JOptionPane.showConfirmDialog(this, "successful");
                filltable();
                  edt_name.setText("");
                  edt_date.setText("");
                   
                      edt_id.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(ADDGU.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_submitbuttonActionPerformed

