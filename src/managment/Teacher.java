    private void filltable() {
        tab.setRowCount(0);
        try {
            PreparedStatement stmt;
            stmt = con.prepareStatement("select teacher.teacher_id,teatcher_name,teacher.username ,teacher.password,class.class_name from teacher,class where class.class_id=teacher.class_id");
            ResultSet rs;
            rs = stmt.executeQuery();
            while (rs.next()) {
                tab.addRow(new Object[]{rs.getInt(1), rs.getString(3), rs.getString(2), rs.getString(4),rs.getString(5)});

     
            }
            tbl_show.setModel(tab);
        } catch (SQLException ex) {
            Logger.getLogger(ADDGU.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
     private  void fillcombobox(){
        try {
            PreparedStatement stmt;
            stmt = con.prepareStatement("select *from class");
            ResultSet rs;
            rs = stmt.executeQuery();
            while (rs.next()) {
                class_id.add(rs.getInt(1));
                cmb.addItem(rs.getString(2));

            }
            tbl_show.setModel(tab);
        } catch (SQLException ex) {
            Logger.getLogger(ADDGU.class.getName()).log(Level.SEVERE, null, ex);
        }
}

