    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        if (!edt_name.getText().isEmpty() && !edt_id.getText().isEmpty() && !edt_username.getText().isEmpty()&& !edt_pass.getText().isEmpty()) {
            String name = edt_name.getText();
            String username= edt_username.getText();
            int id = Integer.parseInt(edt_id.getText());
            String pass = edt_pass.getText();
         
            

           try {
                PreparedStatement stmt;
                stmt = con.prepareStatement("insert into teacher (teacher_id,teatcher_name,username,password,class_id) values(?,?,?,?,?)");

                stmt.setInt(1, id);
                stmt.setString(2, name);
                stmt.setString(3, username);
                stmt.setString(4, pass);
                stmt.setInt(5, class_id.get(cmb.getSelectedIndex()));
                stmt.executeUpdate();
                JOptionPane.showConfirmDialog(this, "successful");
                filltable();
                edt_name.setText("");
                  edt_username.setText("");
                    edt_pass.setText("");
                      edt_id.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(ADDGU.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_addActionPerformed

